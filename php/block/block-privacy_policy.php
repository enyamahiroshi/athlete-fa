<dl class="ordered-list">
  <dt>個人情報の定義</dt>
  <dd>
    <p>このプライバシーポリシーにおいて、個人情報とは生存する個人に関する情報であって、氏名、生年月日、性別、電話番号、メールアドレス、勤務先等、特定の個人を識別し得る情報をいいます。</p>
  </dd>
  <dt>個人情報の取得方法</dt>
  <dd>
    <p>ユーザーがお問い合わせフォーム、エントリーフォームを使用する際に、下記の情報を取得させていただきます。<br>当社による個人情報の収集・利用は、ユーザーの自発的な提供によるものであり、提供された個人情報を当社が本方針に従って利用することをユーザーが承諾したものとします。<br>お問い合わせフォーム：会社名、氏名、メールアドレス、電話番号<br>エントリーフォーム：氏名、メールアドレス、電話番号、履歴書、職務経歴書に記載の内容</p>
  </dd>
  <dt>個人情報の利用目的</dt>
  <dd>
    <p>当社はユーザーから取得した個人情報を下記目的において利用します。</p>
    <ul class="list-disc">
      <li>ユーザーからの各種お問い合わせに対するサポート対応のため</li>
      <li>当社の採用活動で応募者の適格性を評価するため</li>
    </ul>
  </dd>
  <dt>個人情報を安全に管理するための措置</dt>
  <dd>
    <p>当社は個人情報の紛失、破壊、改ざん及び漏洩などのリスクに対して、個人情報の安全管理が図られるよう、従業員に対し必要かつ適切な監督を行います。</p>
  </dd>
  <dt>個人情報の委託、共同利用</dt>
  <dd>
    <p>当社はユーザーへサービスを提供する等の業務遂行上、個人情報の一部を外部の委託先へ提供する場合があります。<br>その場合、業務委託先が適切に個人情報を取り扱うように適切な監督を行います。</p>
  </dd>
  <dt>個人情報の第三者提供について</dt>
  <dd>
    <p>当社は法令及びガイドラインに別段の定めがある場合を除き、ユーザーの同意を得ない限り第三者に個人情報を提供することはありません。</p>
  </dd>
  <dt>保有個人情報の開示、訂正</dt>
  <dd>
    <p>当社は本人から個人情報の開示を求められたときには、遅滞なく本人に対しこれを開示します。<br>提供された個人情報について、修正または削除を求められたときには、本人であることを確認したうえで速やかに対応させていただきます。</p>
  </dd>
  <dt>cookieについて</dt>
  <dd>
    <p>cookieとは、WebサーバーからWebブラウザに送信されるデータのことです。Webサーバーがcookieを参照することでユーザーのパソコンを識別でき、効率的に当社Webサイトを利用することができます。当社Webサイトがcookieとして送るファイルは、個人を特定するような情報は含んでおりません。お使いのWebブラウザの設定により、cookieを無効にすることも可能です。</p>
  </dd>
